import streamlit as st
import pandas as pd
import joblib

# Load model & encoders
model = joblib.load("/mnt/data/mumbai_house__prediction_model.pkl")
encoder = joblib.load("/mnt/data/label_encoder (4).pkl")

st.title("üè† Mumbai House Price Prediction App")

# Inputs
age = st.number_input("Age", 18, 60)

city = st.selectbox("City", encoder["City"].classes_)
area = st.selectbox("Area", encoder["Area"].classes_)
property_type = st.selectbox("Property Type", encoder["property_type"].classes_)

years_of_exp = st.number_input("Years of Experience", 0, 40)

# Create dataframe
df = pd.DataFrame({
    "Age": [age],
    "City": [city],
    "Area": [area],
    "property_type": [property_type],
    "Years of Experience": [years_of_exp]
})

# Predict
if st.button("Predict Price"):

    for col in encoder:
        df[col] = encoder[col].transform(df[col])

    prediction = model.predict(df)

    st.success(f"Predicted House Price: ‚Çπ {prediction[0]:,.2f}")